                    <h4 class="newsH4">The Feil Organization Signs U.S Postal Service for 333 East 14th Street.</h4>
                    <p class="overallP"><span class="newsDateSpan">New York, NY – November 14, 2013</span>&nbsp; The United States Postal Service has signed an agreement
to occupy 8,440 square feet of retail space at 333 East 14th Street, announced The Feil Organization.
The Peter Stuyvesant branch of the U.S.P.S. inked the 10-year sublease for the space which is
located at the base of the residential building on 14th Street and First Avenue, and will occupy 6,940
square feet on the ground floor and 1,500 square feet of basement. It is a relocation of the Postal
Service’s existing 432 East 14th Street branch, states Randall Briskin, Vice President of Leasing at
The Feil Organization.</p>

<p class="overallP overallP_secondP">
<span class="quotations">“The Post Office is a destination in itself and has been downsizing its space needs in response to the
growing use of email and other technologies,” </span>Briskin explains. <span class="quotations">“The new space at 333 East 14th
Street is smaller than, but in close proximity to its current location, allowing the U.S.P.S. to
continue convenient service for Stuyvesant Town, the East Village, and the surrounding area.”</span></p>



<p class="overallP overallP_secondP">
Feil holds a long-term ground lease for the 333 East 14th Street retail space. The site, which offers
100 feet of frontage on 14th Street, had been a Gristede’s and then a Duane Reade after the drug
chain acquired eight of the supermarkets in 2007. Duane Reade moved to a larger location at nearby
245 First Avenue in January 2013, leaving the space available.
</p>

<p class="overallP overallP_secondP">
<span class="quotations">“This is a true neighborhood location for a tight-knit community,” </span>Briskin says. <span class="quotations">“Yet our site is also
close to the excitement and continued growth around Union Square, just a few blocks away.”</span>
</p>


<p class="overallP overallP_secondP">
Other retailers close to 333 East 14th Street include CVS, Vitamin Shoppe, KFC and more. The
building was constructed in 1963 and converted to a cooperative in 1988. The new U.S.P.S. branch
is currently under construction and is scheduled to open in the second quarter of 2014.
</p>

<p class="overallP overallP_secondP">

Inquiries should be directed to Brian Feil, Vice President of Leasing at The Feil Organization at
212.563.6557 x213 <a href="mailto:bfeil@feilorg.com">bfeil@feilorg.com</a>.

</p>





















                    
                    
                    
