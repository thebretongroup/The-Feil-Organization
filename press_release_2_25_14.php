                    <h4 class="newsH4">Feil signs ASSET.TV to 570 Lexington after record 66,000 sq. ft. in 2013 leases.</h4>
                    <img src="images/pr_images/570Lex.jpg" class="prImage"><p class="overallP"><span class="newsDateSpan">New York, NY – February 25, 2014</span> In the first lease of 2014 for its landmark 570
Lexington Avenue building, The Feil Organization has signed Asset.tv to 3,883 square
feet of office space on the building’s 45th floor. The lease caps an exceptional number of
new leases signed by Feil in 2013, and begins great momentum starting 2014.</p>

<p class="overallP overallP_secondP">The new agreement continues what has been an extraordinarily active leasing period for the Midtown building, with some 66,000 square feet of office space signed in 2013,
reports David Turino, Director of Commercial Leasing for Feil, the building’s owner and
manager.</p>

<p class="overallP overallP_secondP">
Asset.tv, a video reporting firm, had been subleasing from another tenant in the building,
and “loved the prebuilt space that was available,” according to Turino. Tyler Owen of
Colliers represented the tenant.</p>

<p class="overallP overallP_secondP"><span class="quotations">“This is a building that can only be found in New York City. Its stunning Art Deco
architecture with a number of setback floors that offer panoramic views of Park Avenue
make this a unique city landmark,” Turino stated. “In addition, our tower section has
floors from 3,800 square feet to 4,200 square feet, allowing smaller companies to have a
full floor presence.”</span></p>


<p class="overallP overallP_secondP">Cornwall Capital Management signed a new lease for 7,570 square feet on the 10th
floor, relocating from the Rockefeller Center area for the above-standard installations at
570 Lexington, said Turino, who along with Brian Feil, Vice President of Leasing,
represented ownership in all the transactions. Keith Caggiano and Roshan Shah of CBRE
represented the tenant.</p>
<p class="overallP overallP_secondP">Represented by Irina Super of Red Real Estate, LLC, Orange, California-based
Strathspey Crown Holdings has taken its first New York City office space at the
building, leasing 4,185 square feet on the 26th tower floor. “This floor is the first tower
setback and features a magnificent terrace overlooking Park Avenue and St.</p>

<p class="overallP overallP_secondP">
<span class="quotations">“Bartholomew’s church, which is adjacent to the building,”</span> Turino said. <span class="quotations">“It has a dramatic
panoramic view of Park Avenue that you cannot find anywhere else in the city and makes
a perfect statement for this healthcare-focused private equity firm.”</span></p>

<p class="overallP overallP_secondP">
Globex International Holdings is relocating from 515 Madison Avenue to 6,514 square
feet on the 15th floor of 570 Lexington. Susann Eaton and Scott Bloom of Bloom Real
Estate Group, LLC represented the tenant. “Like Strathspey, Globex fell in love with the
panoramic Park Avenue views. We worked very hard to carve out just the right space for
them, one that would accommodate their layout and give them the westerly view,” stated
Turino.</p>

<p class="overallP overallP_secondP">
After just two years at 570 Lexington Avenue, law firm Carol McNulty & Kull renewed
and nearly doubled its size, taking more than 32,000 square feet on the 8th and 17th
floors. The tenant was represented by Joseph Harkins of Cushman & Wakefield.
Working with Stephen Berliner of Studley, THL Credit Advisors LLC, an arm of
Thomas H. Lee, a Boston-based private equity firm, will also nearly double its space to
7,758 square feet and relocate to the 21st floor. Another existing tenant Carrera
Willowbridge Capital renewed its 4,205 square foot space on the building’s 31st floor.
Carrera was represented by Joel Burris of Brown Harris Stevens.</p>

<p class="overallP overallP_secondP">
Originally known as the RCA Victor Building, 570 Lexington is one of the city’s great
skyscrapers. A masterpiece of Art Deco architecture, the 50-story structure boasts a
crown of Gothic spires (inspired by radio waves) that resemble a giant torch when
illuminated. The 640 foot tall building, designed by Cross and Cross, opened in 1931 and
was added to the National Register of Historic Places in 2004.</p>

<p class="overallP overallP_secondP">
Neighboring buildings include some of the city’s great corporate and cultural landmarks,
such as the Waldorf-Astoria, MetLife Building, Rockefeller Center, and St. Patrick’s
Cathedral. It is located steps from the 6, E, and M subway lines.</p>

<p class="overallP overallP_secondP">
According to Turino, the building is now 91.5 percent leased, and Feil is pre-building
several of the tower floors to continue its 2014 leasing efforts.
<span class="quotations">“This building is one of the great office values in New York, significantly less expensive
than Park Avenue, but with some distinct advantages, including panoramic vistas and
stunning architecture,” Turino said. “There is no better building in Midtown for anyone
looking for a classic New York experience.”</span>

</p>
