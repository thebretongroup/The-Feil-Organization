                    <h4 class="newsH4">The Feil Organization announces completion of multi-million dollar renovations at 7 Penn Plaza and 488 Madison Avenue.</h4>
                    <img src="images/pr_images/7Penn.jpg" class="prImage"><p class="overallP"><span class="newsDateSpan">New York, NY – March 5, 2013, 2013</span> &nbsp;The Feil Organization is pleased to announce the
completion of a major lobby and entryway renovation at 7 Penn Plaza and lobby renovation at 488
Madison Avenue. Construction commenced for both buildings in 2012 and the appointed architect
was Goldstein, Hill & West Architects, LLP.</p>

<p class="overallP overallP_secondP">
7 Penn Plaza is a 18-story commercial office building located on 7th Avenue in the emerging Penn
Plaza District, one of New York City’s most up-and-coming commercial locations. Built in 1921,
the newly renovated property now features a grand entryway with a soaring 16’ entrance, modern
reception desk, cove lighting, striking granite finishes, modernized elevators, and renovated
corridors and restrooms. The Midtown South building is located directly across the street from
Madison Square Garden and Penn Station, North America’s busiest train station with 300,000 riders
daily. The 7 Penn Plaza renovations coincide with several of the area’s ongoing developments such
as Moynihan Station, Brookfield’s office building project, the Hudson Yards Special Purpose
District, and the No. 7 train extension.
</p>


<p class="overallP overallP_secondP">
The new lobby at 488 Madison Avenue features beautiful honed gray limestone with a polished
white Calcutta stone border. The elevators are framed with satin-finished stainless steel and the
entire lobby features eco-friendly LED lighting. The 23-story building overlooks St. Patrick’s
Cathedral and is among New York City’s most eclectic and richest architecture. The classic 447,000
square foot building was designed in 1949 for Look Magazine by Emery Roth & Sons, and reached
Landmark status in 2010. It has been owned and operated by The Feil Organization for over 30
years.

</p>


<p class="overallP overallP_secondP">
According to David Turino, Director of Commercial Leasing at The Feil Organization, <span class="quotations">“We are
always striving to improve our buildings. The renovations at both 7 Penn Plaza and 488 Madison
Avenue are indicative of our commitment to keep our properties fresh and modern while providing
our tenants with the best environments possible.”</span>
</p>


<p class="overallP overallP_secondP">
7 Penn Plaza and 488 Madison Avenue are owned and managed by The Feil Organization, a
private, established commercial real estate firm headquartered at 7 Penn Plaza. A powerful force in
real estate for over 50 years, the company owns and operates a nationwide portfolio of over 26
million square feet of retail and commercial space, and over 5,000 residential rental units. Feil also
owns such Manhattan landmarks as the Fred F. French Building and 570 Lexington Avenue. The
organization maintains regional offices in Chicago, IL; New Orleans, LA; St. Petersburg, FL; and
on Long Island in Levittown, NY.
</p>


