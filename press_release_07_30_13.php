                    <h4 class="newsH4">The Feil Organization names Forelli director of retail leasing.</h4>
                    <img src="images/pr_images/NFpic.jpg" class="prImage"><p class="overallP"><span class="newsDateSpan">New York, NY – July 30, 2013</span> &nbsp;The Feil Organization has named industry veteran
Nicholas Forelli Director of Retail Leasing, the company announced today.</p>

<p class="overallP overallP_secondP">
In this role, Forelli will focus on Feil’s leasing efforts throughout the metropolitan area,
including New York City, Long Island and New Jersey.
</p>



<p class="overallP overallP_secondP">
<span class="quotations">“I’m thrilled to join the great team that Brian Feil and Randy Briskin have created here,”
Forelli said. “I’ve known the company for many, many years, and it’s always at the top of
any list of places to work because of its reputation and properties. It’s a particularly great
fit for me, as I’ve also worked in many other markets around the country where Feil also
has properties.”</span>

</p>

<p class="overallP overallP_secondP">
Chief among Forelli’s projects will be Glen Oaks Shopping Center in Queens, working
with Briskin on the redevelopment of Concourse Plaza in the Bronx, smaller retail
projects around the metro area, as well as One & Olney Square in Philadelphia, he noted.
“We are extremely excited about adding Nick to our team. Given his background, we
know that he will be a strong asset to the future growth of the company,” said Brian Feil,
a vice president of leasing at the company.
</p>


<p class="overallP overallP_secondP">
Prior to joining The Feil Organization, Forelli served as a vice president at Muss
Development LLC. He also has served as director of leasing for Rosen Associates
Management Corp. and in leasing/sales positions with Welco Realty and Massey Knakal
Realty Services.
</p>








<p class="overallP overallP_secondP">

Inquiries should be directed to Brian Feil, Vice President of Leasing at The Feil Organization at
212.563.6557 x213 <a href="mailto:bfeil@feilorg.com">bfeil@feilorg.com</a>.

</p>



























                    
                    
                    
