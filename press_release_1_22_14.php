                    <h4 class="newsH4">Feil Organization announces 233,000 sq. ft. renewal at Queens Atrium.</h4>
                    <img src="images/pr_images/Queens-AtriumPR.jpg" class="prImage"><p class="overallP"><span class="newsDateSpan">New York, NY – January 22, 2014</span> &nbsp;The Feil Organization has inked a 233,000 square foot lease
renewal and expansion with the New York City Department of Design and Construction at Queens
Atrium, 30-30 Thomson Avenue in Long Island City, NY.</p>

<p class="overallP overallP_secondP">The 15-year agreement, signed by the City of New York Department of Citywide Administrative
Services on behalf of the Department of Design and Construction (DDC), is a renewal of the
department’s existing approximate 210,000 square feet space plus an expansion of 23,000 square feet
for a total of 233,000 square feet. The DDC will remain in what is an up-and-coming area of the city,
states Randall Briskin, Feil’s Vice President of Leasing, who represented the landlord, a group led by
The Feil Organization.</p>

<p class="overallP overallP_secondP">
The DDC is the City's primary capital construction project manager and provides communities with
new or renovated structures such as firehouses, libraries, police precincts, courthouses and senior
centers, as well as roadway, sewer and water main construction projects in all five boroughs. The
department will occupy multiple floors of the six-level facility.
</p>




<p class="overallP overallP_secondP">
A former industrial building that dates back to the first quarter of the century, Queens Atrium has been
artfully redeveloped into modern showroom, office and conference space. The building is ideally
located in the heart of Long Island City, minutes from Long Island and midtown Manhattan in an
emerging residential and commercial section of Queens. <span class="quotations">“The area is rapidly changing with residential
construction. Long Island City is slated to see the construction of over 8,000 units of residential
housing, as well as a flurry of new stores and restaurants,” </span>Briskin says. <span class="quotations">“Additionally, there has been
a migration of ‘back office users’ looking to mitigate the rising occupancy costs of Manhattan.”</span>
</p>


<p class="overallP overallP_secondP">
Formerly occupied by The American Chiclet Company, Queens Atrium was master planned by Ieoh
Ming (I. M.) Pei, the world-renowned modernist architect. Queens Atrium features a restaurant,
conference facilities, concierge desk, and lobby security cameras. It is one of the few buildings in the
city with its own bus service operating continuously throughout the day to local transportation hubs,
including the 7, E, F, G, N and R subway lines, three bus routes and the Long Island Railroad Hunters
Point station.</p>

<p class="overallP overallP_secondP">
The tenant was represented by Rachna Desi-Jain, Doug Holowink, Michael Lee, John Morrill, John
Reinertsen, Keith Thompson and Sean Wilson of CBRE.
</p>

<p class="overallP overallP_secondP">
<span class="quotations">
“Dealing with the unique demands of a governmental agency can always be challenging,”</span> Briskin says.
<span class="quotations">“This deal was long and arduous, but all parties rose to the occasion in the spirit of insuring that our
existing relationship as landlord to the agency could be continued for the next 15 years.”</span>
</p>


<p class="overallP overallP_secondP">

