                    <h4 class="newsH4">The Feil Organization Announces the completion of the new facade renovations at KOHL'S Plaza in Levittown, NY.</h4>
                    <img src="images/pr_images/Kohls-Plaza_7548.jpg" class="prImage"><p class="overallP"><span class="newsDateSpan">New York, NY – December 13, 2013</span>&nbsp; The Feil Organization is pleased to announce the
completion of a two million dollar façade renovation at Kohl’s Plaza. The Plaza is located at 3575
Hempstead Turnpike in Levittown, NY. This is the first major renovation for the 28-year old
Nassau County, Long Island shopping center and includes a new brick façade, columns, signage,
and exterior lighting. The six-month construction project commenced in April and was completed
last month.</p>

<p class="overallP overallP_secondP">Tenants at the shopping center include Kohl’s, Party City, PetSmart, Five Below, Famous
Footwear, Dress Barn, GNC and GameStop. The retail property shares parking via a cross easement
with both a Loews Cineplex and with the Nassau Mall. The overall site features a BJ’s Wholesale
Club, LA Fitness, Michaels, Best Buy, Gap, Old Navy, DSW Shoes, TGIF, Wendy’s, Sports
Authority, and a five-story office building</p>


<p class="overallP overallP_secondP">
