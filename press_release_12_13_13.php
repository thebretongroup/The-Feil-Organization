<p class="overallP overallP_secondP">
Randall Briskin, Vice President of Leasing at The Feil Organization stated, <span class="quotations">“The renovation gives
the shopping center a new modern, updated look that will help continue to maintain the property’s
historical success going forward. Although the renovation wasn’t necessary, we felt the
enhancement would benefit the center and surrounding community, and enhance the overall
shopping experience for our customers.”</span>
</p>


<p class="overallP overallP_secondP">
Kohl’s Plaza is located on the heavily traveled Hempstead Turnpike at the intersection of Hamlet
Road in Levittown, midway between the villages of Hempstead and Farmingdale. The site is
accessible from the Wantagh, Southern, Northern and Bethpage State Parkways. Nassau County
was sited as the 12th richest county in America in 2012 by Forbes magazine, with an annual median
household income of $91,000. Levittown boasts a median household income of $150,900.
</p>



<p class="overallP overallP_secondP">
The Feil Organization acquired the Kohl’s Plaza/Nassau Mall retail site in 1984. Both centers are
97% leased with availabilities ranging from 2,250 to 7,500 square feet. Feil also owns the on-site
office building, 3601 Hempstead Turnpike, which features approximately 95,450 square feet on six
floors.
</p>

<p class="overallP overallP_secondP">
Kohl’s Plaza is owned and managed by The Feil Organization, a private, established commercial
real estate firm headquartered at 7 Penn Plaza, New York, New York 10001. A powerful force in
real estate for over 60 years, the company owns and operates a nationwide portfolio of over 26
million square feet of retail and commercial space, and over 5,000 residential rental units. Feil also
owns such Manhattan landmarks as the Fred F. French Building and 570 Lexington Avenue. The
organization maintains regional offices in Chicago, IL; New Orleans, LA; St. Petersburg, FL; and
on Long Island in Levittown, NY.
</p>

