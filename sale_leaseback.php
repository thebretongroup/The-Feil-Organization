                    <p class="overallP">The Feil Organization devotes a substantial portion of its investment portfolio to net leased property acquisitions. Net leases offer many of the advantages of property ownership without tying up capital. When retail companies or other types of business own the properties on which they operate, a major part of what could be working capital is permanently unavailable. A simple sale would free that capital, but it would also leave the business without the use of property.</p>
<p class="overallP overallP_secondP">
Fortunately, there is a solution: The retailer or business owner can sell its single-user property to an investor, and then lease it back under a net lease agreement. This arrangement enables the business to gain full use of the working capital, while continuing to operate with virtually the same autonomy enjoyed as a property owner.</p>
