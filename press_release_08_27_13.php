                    <h4 class="newsH4">Feil and Savanna Announce DMV deal and 93 percent occupancy at 21 Penn Plaza.</h4>
                    <img src="images/pr_images/21_Penn_Plaza.jpg" class="prImage"><p class="overallP"><span class="newsDateSpan">New York, NY – August 27, 2013</span> &nbsp;The Feil Organization, a New York-based private
real estate investment firm, and Savanna, a New York-based private equity and asset
management firm, have signed the New York State Department of Motor Vehicles to
27,455 square feet of retail and office space at 21 Penn Plaza, the companies announced
today. Combined with other recent transactions, the building’s occupancy rate has soared
from 78 percent to 93 percent in less than two months.</p>

<p class="overallP overallP_secondP">
The DMV will occupy 1,385 square feet of retail space on the first floor, which
will serve as a private entrance for its 26,070-square-foot customer service facility on the
second level, says David W. Turino, Director of Commercial Leasing for The Feil
Organization. Terms of the 10-year lease were not disclosed.</p>



<p class="overallP overallP_secondP">
Located on Ninth Avenue between 30th and 31st Streets, 21 Penn Plaza was
acquired by a partnership of The Feil Organization and Savanna in 2011, and features
two distinctive, recently renovated lobbies. <span class="quotations">“In essence, this part of 21 Penn Plaza is a
building within a building. The private entrance allows the DMV its own identity
separate from the office tenant population utilizing the Ninth Avenue and 31st Street
lobbies,” </span>Turino says.
</p>

<p class="overallP overallP_secondP">
The DMV office will relocate from its current Herald Square location next spring.
Turino represented the Landlord in the transaction, while Jeffrey Rosenblatt of Newmark
Grubb Knight Frank represented the State of New York.</p>


<p class="overallP overallP_secondP">
This latest agreement is the third major lease to be completed in 45 days at the 17-
story full-service building. Earlier this month, the corporate office for a major department
store expanded its presence by 26,000 square feet for a total of 89,000 square feet at 21
Penn Plaza, and Tutor Perini Corp. has also signed for additional space.</p>

<p class="overallP overallP_secondP">
<span class="quotations">“This dramatic increase is a testament to the quality of the building and its
location,”</span> Turino says. <span class="quotations">“We expect to be fully leased shortly.”</span>
</p>


<p class="overallP overallP_secondP">
Situated just one half-block from Penn Station, 21 Penn Plaza is easily accessible
by the Long Island Railroad, Amtrak, NJ Transit, Port Authority Bus Terminal, PATH to
NJ, Lincoln Tunnel, West Side Highway, and all of the Westside subway and bus lines.
In addition, 21 Penn Plaza sits at the front door of the West Side Railyards Special
Improvement District, including the building of Hudson Yards, Manhattan West, and the
new Moynihan Station, as well as the expansion of the High Line Park and the extension
of the IRT 7 subway line to 10th Avenue.
</p>




<p class="overallP overallP_secondP">

