                    <h4 class="newsH4">The Feil Organization signs 70,000 sq. ft. of office leases at 257 Park Avenue South.</h4>
                    <img src="images/pr_images/257Park-Ave.jpg" class="prImage"><p class="overallP"><span class="newsDateSpan">New York, NY – April 29, 2013</span> &nbsp;The Feil Organization has signed two major leases
totaling nearly 70,000 square feet at 257 Park Avenue South, the company announced
today.</p>

<p class="overallP overallP_secondP">
The Environmental Defense Fund (EDF) has signed a long-term renewal for 44,010
square feet, and plans to implement several sustainability features in the space, says
Robert Fisher, Director of Commercial Leasing for Feil, the owner and manager.
</p>


<p class="overallP overallP_secondP">
<span class="quotations">
“This is the perfect site and perfect building for the Fund to be in. We have a great
location near Union Square and Gramercy Park and 13.5-foot ceilings that offer
extraordinary amounts of natural light and spectacular views of Gramercy Park,”</span> Fisher
says. The building’s dramatic architecture features brick masonry and terracotta adorned
by gargoyles and Art Deco graphics.
</p>


<p class="overallP overallP_secondP">
The nonprofit Environmental Defense Fund (EDF) is a national organization that seeks to
create transformational solutions to the most serious environmental problems by linking
science, economics, law and innovative private-sector partnerships. The EDF has been a
tenant at 257 Park Avenue South, also known as the Gramercy Park Building, since 1996.
In addition, Madison Logic has signed for 24,562 square feet, a significant expansion of
its existing space at the building, Fisher adds. The company is a provider of data-powered
lead solutions for world-class B2B advertisers.
</p>


<p class="overallP overallP_secondP">
<span class="quotations">“Madison Logic is a growing technology company and the area around 257 Park Avenue
South increasingly is becoming a high-tech corridor in the city,” </span>Fisher notes. <span class="quotations">“It’s a
terrific match.”</span>
</p>


<p class="overallP overallP_secondP">
Built in 1912, the 20-story building is located at 21st Street and Park Avenue South in the
heart of the increasingly active Flatiron District/Union Square/Gramercy Park area.
Landmarks and sites in the neighborhood include the legendary Pete’s Tavern, Baruch
College, Shake Shack, the Flatiron Building, and Gramercy, Union Square and Madison
Square Parks.
</p>


<p class="overallP overallP_secondP">
Other tenants at 257 Park Avenue South include: Team Detroit, a division of WPP;
SpaFinder Wellness; and FedEx Office Print & Ship Center. Services at the building
include a 24-hour concierge desk and security cameras in the elegant lobby.
Approximately 7,500 square feet of the full-service building’s 226,000 square feet is
available for lease.
</p>


<p class="overallP overallP_secondP">
Brian Feil, Vice President of Leasing for The Feil Organization, and Fisher represented
Feil in both transactions. Brian Palumbo and Michele Frankel worked as in-house
counsel for Feil. Alan Desino, Executive Managing Director of Colliers International,
represented the EDF, while Marc Ellman, President of Ellman Realty Advisors,
represented Madison Logic.
</p>








<p class="overallP overallP_secondP">

