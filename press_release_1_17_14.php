                    <h4 class="newsH4">PROHEALTH to open urgent care facility at Glen Oaks Shopping Center.</h4>
                    <img src="images/pr_images/Glen-Oaks.jpg" class="prImage"><p class="overallP"><span class="newsDateSpan">New York, NY – January 16, 2014</span>&nbsp; PROHEALTH has signed a lease to occupy 259-27 Union
Turnpike located in the Glen Oaks Shopping Center in the Glen Oaks section of Queens, announced
The Feil Organization, the center’s owner and manager. The agreement brings the open-air project
to 99 percent leased.</p>

<p class="overallP overallP_secondP">The medical services provider will occupy 10,913 square feet at the east endcap of the 187,650-
square-foot center and is expected to open in February or March. The rent was not disclosed.
The tenant is renovating the façade and expanding the existing space to increase its frontage on
Union Turnpike.</p>


<p class="overallP overallP_secondP">
<span class="quotations">“PROHEALTH is the perfect urgent care medical facility to fill a void in the marketplace and continue
our goal to bring quality services to the area in and around Glen Oaks. The location made a great
deal of sense for PROHEALTH with their headquarters located in nearby Lake Success,” </span>stated
Nicholas Forelli, Director of Leasing of The Feil Organization, who represented the landlord.
PROHEALTH was represented by Todd Cooper of Ripco Real Estate.
</p>

<p class="overallP overallP_secondP">
PROHEALTH is one of the largest integrated physician group practices in the New York metropolitan
area, with offices throughout New York City and Long Island. Its state-of-the-art medical facilities
are equipped with the most advanced technology available offering patients a variety of services
and treatments. In December 2013, North Shore-LIJ CareConnect, the health system’s newly
established insurance company, announced that PROHEALTH Care Associates and its affiliates have
joined CareConnect’s provider network. This adds to the importance of PROHEALTH to this area.
</p>


<p class="overallP overallP_secondP">
Located between 255th Street and 260th Street on heavily trafficked Union Turnpike, Glen Oaks
Shopping Center consists of three buildings over a five-block span. It is situated close to the
Queens/Nassau County border, making it accessible to a population of 767,114 people within a
five-mile radius, boasting an average household income of more than $105,000.
</p>

<p class="overallP overallP_secondP">
<span class="quotations">“This center is in a very busy area bordering Eastern Queens and Western Nassau county, which
gives it exposure to the high density demographic of the Queens population along with the high
income population of Nassau county,” </span>Forelli added.
</p>

<p class="overallP overallP_secondP">
Shoppers also can reach the center from the Q46, QM5, QM6 and QM8 buses, the Littleneck, Cross
Island and Grand Central Parkways, and Interstate 495 (the Long Island Expressway).
</p>

<p class="overallP overallP_secondP">
Other retailers at the center include anchors Burlington Coat Factory and Waldbaums. Mandee,
Rite-Aid, Duane Reade, Petland, McDonald’s, Subway, Sleepy’s, HSBC, and Chase are also part of
this prestigious group of tenants.
</p>


<p class="overallP overallP_secondP">

Inquiries should be directed to Randall Briskin, Vice President of Leasing at The Feil Organization
at 212.563.6557 x218 <a href="mailto:rbriskin@feilorg.com">rbriskin@feilorg.com</a>.

</p>











                    
                    
                    
