                    <h4 class="newsH4">Feil and Savanna sign SAKS & Co to full floor expansion at 21 Penn Plaza.</h4>
                    <img src="images/pr_images/21_Penn_Plaza.jpg" class="prImage"><p class="overallP"><span class="newsDateSpan">New York, NY – August 20, 2013</span> &nbsp;Saks & Co. has signed a lease for 26,070 square feet
on the entire fifth floor of 21 Penn Plaza, announced The Feil Organization, the
building’s manager and owner.</p>

<p class="overallP overallP_secondP">
The expansion gives Saks a total of 89,000 square feet at 21 Penn Plaza, making it
the largest tenant at the 380,000-square-foot building, says David W. Turino, director of
commercial leasing for The Feil Organization. The retailer already occupies full floors on
the 6th, 16th and 17th floors of the building. Terms of the deal were not disclosed, but the
asking rent for the space was $46 per square foot.
</p>



<p class="overallP overallP_secondP">

<span class="quotations">“Saks required more space for the growth of its online marketing and
merchandising staff and we were pleased to accommodate them with a six-year lease for
this new additional space,”</span> Turino explains. <span class="quotations">“It is one of a number of tenants – and now
the largest – to expand their presence at 21 Penn Plaza.”</span>
</p>

<p class="overallP overallP_secondP">
Major tenants at the building include Joseph R. Loring Associates, Langan
Engineering, and Tutor Perini Corp., the last of which also recently expanded its presence
at 21 Penn Plaza.
</p>


<p class="overallP overallP_secondP">
Built in 1931, the 17-story structure is located on 9th Avenue between 30th and
31st Streets and was acquired by a partnership of The Feil Organization and Savanna
Funds in 2011. The full service office building features two recently renovated distinctive
lobbies with new illuminated blade signage. A commuter’s dream, 21 Penn is situated
just one half-block from Penn Station and is easily accessible by the Long Island
Railroad, Amtrak, NJ Transit, Port Authority Bus Terminal, PATH to NJ, Lincoln
Tunnel, West Side Highway, and all of the Westside subway and bus lines.
</p>


<p class="overallP overallP_secondP">
21 Penn Plaza sits at the front door of the Westside Rail Yards Special
Improvement District Turino adds, including the building of Hudson Yards, Manhattan
West, and the new Moynihan Station, as well as the expansion of the High Line Park and
the extension of the IRT 7 subway line to 10th Avenue.
</p>

<p class="overallP overallP_secondP">
<span class="quotations">“The Westside Rail Yards development is happening here, and we’re right in the
thick of things,” </span>Turino states. <span class="quotations">“We had multiple bids for the Saks space, and expect to be
close to 100 percent leased by year-end.”</span>
</p>



<p class="overallP overallP_secondP">
Ownership was represented by David W. Turino of The Feil Organization and
Saks & Co. was represented in-house by Vincent A. Corno, senior vice president of real
estate for Saks International.
</p>




<p class="overallP overallP_secondP">

