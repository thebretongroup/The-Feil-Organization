                    <h4 class="newsH4">VALPAK® Signs Lease for New Corporate Headquarters.</h4>
                    <img src="images/pr_images/570Lex.jpg" class="prImage"><p class="overallP"><span class="newsDateSpan">Largo, FL April 9, 2014</span> &nbsp;Valpak Direct Marketing Systems, Inc., a leader in local print and digital coupons has been in the business of helping people save money for more than 45 years, announces it will move its corporate headquarters from Largo to St. Petersburg later this year.</p>

<p class="overallP overallP_secondP">Valpak has signed a lease at the Pasco Building, 805 Executive Center Drive West in Baypoint Commerce Center, a Feil Organization property. The lease will begin on Dec. 7, 2014 and is signed for eight years.

</p>

<p class="overallP overallP_secondP">
This will move the former Cox Target Media and Valpak corporate offices, which the company rented from Harrod Properties in Largo since the early 1990s. Approximately 325 employees will make the move, including those from franchise support, national sales, the executive group, business finance, marketing, IT and graphic design.</p>


<p class="overallP overallP_secondP">
Since the Valpak Manufacturing Center, a 470,000-square-foot, state-of-the-art printing facility, opened in the winter of 2007, the company has planned to move the rest of its employees closer to the manufacturing center, knowing the Largo lease would expire in 2014.
</p>


<p class="overallP overallP_secondP"><span class="quotations">“The new St. Petersburg location not only gives us closer proximity to the VMC, but also allows us to draw from a larger, diverse work pool with access to more of the Tampa Bay area workforce,”</span>said Michael Vivio, president of Cox Target Media, providers of Valpak. <span class="quotations">“We decided on St. Petersburg because we like the cluster of tech companies in the St Petersburg area along with the energy and quality of people,”</span>said Vivio. </p>


<p class="overallP overallP_secondP">
This will be the third corporate headquarters for the company, which began in 1968 in Clearwater and founded by Terry Loebel. The company is now owned by parent company Cox Media Group and Cox Enterprises in Atlanta. 
</p>
<p class="overallP overallP_secondP">The three-story building is located just south of Gandy Blvd. between 4th Street and MLK Street North. Valpak will occupy approximately 50,000 square feet of the Pasco building.

</p>

<p class="overallP overallP_secondP">
Junto Design Studio of Tampa will handle the architectural and interior design work associated with the new space, which will be designed in an open office concept.

</p>

<p class="overallP overallP_secondP">
The Baypoint Commerce Center has 15 office buildings in the park, encompasses 76 acres and was built in 1971.

</p>


                    
                  
